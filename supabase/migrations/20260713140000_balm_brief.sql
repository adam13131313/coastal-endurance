## Concept

A shea-butter-heavy balm for the roughest, most weathered skin on the body: elbows, knees, knuckles, heels. Thick, protective, naturally derived, zero fragrance. Rubbed into the parts that crack, split and callous from years of physical, outdoor work.

This is the most direct line extension of the three ideas on file. It keeps the exact customer, the exact equipment-maintenance frame, and simply moves it from the face to the hardest-wearing zones on the same body. Field Oil maintains the skin that shows; Barrier Balm maintains the skin that takes the impact. Together they read as a small system rather than two unrelated products.

## Why it fits

No new audience, no new frame, no new regulatory lane. The men who buy Field Oil have exactly the elbows, knees and knuckles this is built for, and the positioning language transfers word for word: protection and maintenance for skin that has done decades of fieldwork, not a cosmetic or a cure. It is the lowest-friction way to give the existing customer a reason to buy a second product, and it opens an obvious kit / cross-sell story (oil for the face, balm for the tough zones).

## Nature of the opportunity

The balm shelf is polarised much like the coat-care shelf: cheap petroleum-based repair balms at one end, medicated fissure creams at the other, and very little that is natural, honestly made and aimed squarely at the physically weathered man in the middle. A shea-forward, no-fragrance, design-led balm positioned as equipment maintenance sits cleanly in that gap.

Like the pets idea, the real value is in **deepening an acquired relationship** rather than winning a large independent market. The difference is that this one shares the customer *and* the regulatory lane *and* likely some of the formula, which makes it materially cheaper and faster to bring to a testable state.

## What transfers, what changes

Transfers cleanly: the brand, voice and positioning; the same customer; the AICIS cosmetic pathway already understood from Field Oil; the antioxidant system (Vitamin E plus rosemary CO2) essentially unchanged; and probably several carrier oils (hemp, sunflower, meadowfoam) as the liquid fraction of the balm. Suppliers largely carry over.

Changes: the product is anhydrous and solid rather than a pourable oil, so it needs butters and a hardening agent, a hot-process (melt-and-pour) manufacturing method rather than cold blending, a different fill format (tin or jar, sold by weight), and a different price and pack logic. Comedogenicity, the key constraint on the face, is irrelevant here — which is exactly why richer butters that would be too heavy for Field Oil are ideal for elbows and knees.

## Formulation direction

Structurally a balm is a butter-and-oil base set with a wax. Likely shape: shea butter as the hero, possibly a second harder butter (cocoa or kokum) for structure, a liquid fraction drawn from the existing oil palette for slip and fatty-acid content, a hardening wax, and the transferred antioxidant system. Because it is anhydrous (no water), microbial risk is low and no preservative system is needed — a genuine simplification versus a water-based cream.

Two forks to resolve:

- **Wax choice is a brand decision, not just a technical one.** Beeswax is the traditional balm hardener and performs beautifully, but it is animal-derived and would be the first non-plant ingredient in the range. Candelilla or carnauba (plant waxes) keep the range fully plant-based at a slightly different feel. This turns on whether Coastal Endurance is committed to a vegan / plant-only story, which should be settled deliberately rather than by default.
- **Hemp ratio and oxidation.** Hemp's high PUFA content is the same shelf-life pressure it is in Field Oil. In a balm meant to sit in a tin and be opened repeatedly, the antioxidant system and a sensible hemp ceiling matter, and the argon-blanketing discipline from Field Oil likely applies to the melt.

Allergen note: if macadamia is reused it carries the same tree-nut declaration. Shea itself is technically a tree-nut-derived butter and is worth an allergen line even though it is widely tolerated.

## Format and unit economics

A balm is sold by weight into a tin or jar. Application targets small but tough zones (elbows, knees, knuckles, heels) with a firm smear, so per-use amount is modest but usage is frequent. A likely fill is in the **50–100g** range.

The economics are friendlier than the pets idea and arguably than Field Oil. The base is cheaper (shea is inexpensive; no premium rosehip or unrefined golden jojoba is required to make the product work on an elbow), the same premium customer tolerates a sensible price, and there is no per-millilitre race to the bottom of the kind the dog product forces. No preservative system and simple packaging keep the build cost down.

