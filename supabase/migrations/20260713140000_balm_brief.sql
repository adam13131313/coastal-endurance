## Pricing logic

Priced as a quality natural balm for a premium customer: a respectable absolute price justified by the shea-forward, honestly-made, fragrance-free positioning, but read as sensible rather than luxury. It also unlocks a **bundle** lever — Field Oil plus Barrier Balm as a maintenance kit — which can lift average order value without discounting either product on its own. Exact ladder falls out of the economics once modelled.

## Regulatory pathway

Stays in the familiar lane. A no-therapeutic-claims cosmetic balm sits under the same AICIS framework and ACCC cosmetics information standard already navigated for Field Oil. No APVMA, no new regime.

The one watch-item is claims language. Protection, conditioning and barrier *support* for rough, weathered skin are fine. Claims to *repair*, *heal* or *treat* cracked or fissured skin edge toward a therapeutic (TGA) claim and must be avoided — the same "maintenance not treatment" discipline that governs Field Oil keeps this clean. "For skin that cracks and splits" is safe as a description of who it is for; "heals cracked skin" is not.

## Key risks

The primary risk is again **focus and timing**, identical to the other parked ideas: Field Oil is not yet launched, the batch worksheet is outstanding, and Father's Day is due to run. This is the reason to park, not a reason the idea is weak.

Beyond that the risks are modest and mostly formulation choices rather than existential unknowns: the vegan/wax decision, hemp oxidation management in a repeatedly-opened tin, and a new hot-process manufacturing SOP and QC checklist distinct from the cold-blend oil process.

## Open questions

1. Is Coastal Endurance committed to a fully plant-based / vegan range? This decides beeswax vs plant wax and should be answered as a brand position.
2. Which Field Oil carrier oils carry into the balm, and at what point does reuse simplify sourcing versus a purpose-built cheaper base?
3. Target fill (50g vs 100g) and realistic uses per tin?
4. Is the intended play a standalone product, a bundle with Field Oil, or both?
5. Does the modelled contribution margin clear a worthwhile threshold — and is it better or worse than simply selling more Field Oil?
6. New hot-process manufacturing and QC requirements at 700-unit-equivalent scale.

## Recommendation and next trigger

Park, but rank it above the pets idea for eventual sequencing. Of the extensions on file this is the cheapest, fastest and lowest-risk to bring to a testable state, because it shares the customer, the regulatory lane and much of the formula. Re-open once Field Oil is live and the Father's Day launch is executed, with the bundle/kit angle as the most promising commercial hook to test first.

